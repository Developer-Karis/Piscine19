#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	delete_end_characters(int a, int b, int c)
{
	if (a != '7' || b != '8' || c != '9')
	{
		write(1, ", ", 2);
	}
}

void	ft_print_comb(void)
{
	int a;
	int b;
	int c;

	a = '0';
	while (a <= '7')
	{
		b = a + 1;
		while (b <= '8')
		{
			c = b + 1;
			while (c <= '9')
			{
				ft_putchar(a);
				ft_putchar(b);
				ft_putchar(c);
				delete_end_characters(a, b, c);
				c++;
			}
			b++;
		}
		a++;
	}
}
